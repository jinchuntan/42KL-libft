/* Declaring the library function header */
#include "libft.h"

/*
Function: 
Counting the length of the string

Input:
str: A pointer to a string

Output:
The counter (length of the string)
*/

size_t	ft_strlen(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}
