/* Declaring the library function header */
#include "libft.h"

/*
Function: 
Outputs the string 's' to the given file descriptor

Input:
s: The string to output
fd: The fi;e descriptor on which to write

Output:
None
*/
void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
}
